export const getRandomShameVerses = (count: number = 4): ShameVerse[] => {
  const shuffled = [...shameVerses].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getRandomShameVersesExcluding = (count: number = 4, excludeIds: string[] = []): ShameVerse[] => {
  const availableVerses = shameVerses.filter(verse => !excludeIds.includes(verse.id));
  
  // If we don't have enough unused verses, reset the exclusion list
  if (availableVerses.length < count) {
    console.log('Not enough unused verses, resetting exclusion list');
    return shameVerses.sort(() => Math.random() - 0.5).slice(0, Math.min(count, shameVerses.length));
  }
  
  const shuffled = [...availableVerses].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
};

export const getVersesByCategory = (category: string): ShameVerse[] => {
  return shameVerses.filter(verse => verse.category === category);
};

export const getVerseById = (id: string): ShameVerse | undefined => {
  return shameVerses.find(verse => verse.id === id);
};

